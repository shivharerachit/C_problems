/* Write a C program to accept two Integers and check whether they are equal or not. */
#include<stdio.h>
 void main()
{
 int a,b;
 printf("Enter the two integers:");
 scanf("%d%d",&a,&b);
 if (a==b)
 {
 printf("Both the numbers are equal.");
 }
 else
 {
 printf("Both the numbers are not equal.");
 }
}
