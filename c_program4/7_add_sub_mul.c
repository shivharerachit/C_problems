/* C Program to compute Addition Substraction and Multiplication. */


#include<stdio.h>
 void main()
{
 int a,b;
 printf("Enter the two values:");
 scanf("%d%d",&a,&b);
 printf("Sum of two no. is = %d\n",a+b);
 printf("Subtraction of two no. is = %d\n",a-b);
 printf("Multiplication of two no. is = %d\n",a*b);
}
